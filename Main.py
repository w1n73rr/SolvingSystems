import numpy as np

# Данные
E1 = 12   # Вольт
E2 = 48   # Вольт
E3 = 6    # Вольт
R02 = 0.4 # Ом
R03 = 0.4 # Ом
R1 = 2.5  # Ом
R2 = 1    # Ом
R3 = 4    # Ом
R4 = 15   # Ом
R5 = 2    # Ом
R6 = 2    # Ом

# Матрица коэффициентов A с исправленными элементами
A = np.array([
    [(1/R1 + 1/(R2 + R02) + 1/(R3 + R03)), -1/R1, -1/(R3 + R03)],
    [-1/R1, (1/R1 + 1/R5 + 1/R6), -1/R6],
    [-1/(R3 + R03), -1/R6, (1/(R3 + R03) + 1/R4 + 1/R6)]
])

# Вектор правой части B с исправленными элементами
B = np.array([
    E2/(R2 + R02) - E1/R1 - E3/(R3 + R03),
    E1/R1,
    E3/(R3 + R03)
])

# Решение системы уравнений
phi = np.linalg.solve(A, B)

# Вывод решения
print("Решения для φ1, φ2 и φ3:")
print(f"φ1 = {phi[0]:.4f} В")
print(f"φ2 = {phi[1]:.4f} В")
print(f"φ3 = {phi[2]:.4f} В")
